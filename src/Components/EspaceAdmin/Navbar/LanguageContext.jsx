export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState('FR');

  // Charger la langue depuis localStorage au d√©marrage
  useEffect(() => {
    const savedLanguage = localStorage.getItem('selectedLanguage');
    if (savedLanguage && translations[savedLanguage]) {
      setCurrentLanguage(savedLanguage);
    }
  }, []);

  // Sauvegarder la langue dans localStorage
  const changeLanguage = (languageCode) => {
    setCurrentLanguage(languageCode);
    localStorage.setItem('selectedLanguage', languageCode);
    
    // Optionnel: changer la direction du document pour l'arabe
    if (languageCode === 'AR') {
      document.documentElement.dir = 'rtl';
      document.documentElement.lang = 'ar';
    } else {
      document.documentElement.dir = 'ltr';
      document.documentElement.lang = languageCode.toLowerCase();
    }
  };

  // Fonction pour obtenir une traduction
  const t = (key) => {
    return translations[currentLanguage]?.[key] || key;
  };

  // Fonction pour obtenir la direction du texte
  const getTextDirection = () => {
    return currentLanguage === 'AR' ? 'rtl' : 'ltr';
  };

  const value = {
    currentLanguage,
    changeLanguage,
    t,
    getTextDirection,
    isRTL: currentLanguage === 'AR'
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

// Hook personnalis√© pour utiliser le contexte
export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};